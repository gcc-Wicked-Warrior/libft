#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	while (n--)
	{
		if ((*s1 == '\0' && *s2) || (*s2 == '\0' && *s1) || *s1++ != *s2++)
			return (0);
	}
	return (1);
}
