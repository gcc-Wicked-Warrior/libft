#include "libft.h"

char	*ft_itoa(int n)
{
	char	buff[12] = "00000000000";
	char	*p;
	int		neg;

	neg = 0;
	p = buff + ft_strlen(buff);
	if (n == 0)
		*--p = '0';
	else
	{
		if (n < 0)
			neg = 1;
		while (n != 0)
		{
			if (n < 0)
				*--p = '0' - (n % 10);
			else
				*--p = '0' + (n % 10);
			n /= 10;
		}
		if (neg)
			*--p = '-';
	}
	return (ft_strdup(p));
}
